// if we can create a loop to put 0-9 on screen, how can we count from 9 - 0 

//Counts forward
let myString = [];
for(let i = 0; i < 10; i++) {
    myString.push(i);
}
console.log(myString)

//Counts backwards
let myArray = [];
for(let i = 9; i > 0; i -=1) {
    myArray.push(i);
}
console.log(myArray)
