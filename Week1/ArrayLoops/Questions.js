// Q1. What is a function and why do we use them?

// A function is a block of code designed to perfom a particular task. The function is executed when we call it.
// With functions you can reuse code, define the code once, and then use it many times. 
// you can use the same code many times with different arguments, to produce different results.


// Q2. How do you create a function?

// For a function you need the function keyword, then a name, and followed by parentheses ().
// Example of a Function:

function greet() {
    console.log("Hello there!");
}

// calling the function
greet();